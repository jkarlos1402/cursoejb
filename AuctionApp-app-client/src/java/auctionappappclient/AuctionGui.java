    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        switch (jTabbedPane1.getSelectedIndex()) {
            case 0:
                break;
            case 1: {
                DefaultComboBoxModel model = (DefaultComboBoxModel) auctions_item.getModel();
                List<Item> items = auctionManagerBean.getItems(user.getId());
                model.removeAllElements();
                for (Item item : items) {
                    model.addElement(item);
                }
                refreshAuctions();
            }
            break;
            case 2: {
                DefaultComboBoxModel model = (DefaultComboBoxModel) bid_auction.getModel();
                List<Auction> auctions = startAutionBean.getAuctions();
                model.removeAllElements();
                for (Auction auction : auctions) {
                    model.addElement(auction);
                }
                refreshBids();
                break;
            }
            case 3:
                break;
        }
    }//GEN-LAST:event_jTabbedPane1StateChanged

