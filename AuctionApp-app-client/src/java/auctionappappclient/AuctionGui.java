    public void refreshBids() {
        List<Bid> bids = startAutionBean.getBids();

        DefaultComboBoxModel modelTable = (DefaultComboBoxModel) bid_auction.getModel();
        List<Auction> auctions = startAutionBean.getAuctions();
        modelTable.removeAllElements();
        for (Auction auction : auctions) {
            modelTable.addElement(auction);
        }

        DefaultTableModel model = (DefaultTableModel) bids_jTable.getModel();
        int rows = model.getRowCount();
        for (int i = 0; i < rows; i++) {
            model.removeRow(0);
        }
        for (Bid bid : bids) {
            model.addRow(new Object[]{bid.getBidTime(), bid.getAuction().getItem(), bid.getBidder(), bid.getAmount(), bid.getAuction().getIncrement(), bid.getApproval()});
        }
        bid_amount.setText("");
    }

