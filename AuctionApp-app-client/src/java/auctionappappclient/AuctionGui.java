public void refreshItems() {
        List<Item> items = auctionManagerBean.getItems(user.getId());
        if (items != null) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int rows = model.getRowCount();
            for (int i = 0; i < rows; i++) {
                model.removeRow(0);
            }

            for (Item item : items) {
                model.addRow(new Object[]{item.getId(), item.getDescription(), item.getImage()});
            }
        }
    }

    public void refreshAuctions() {
        List<Auction> auctions = startAutionBean.getAuctions();

        DefaultTableModel model = (DefaultTableModel) auctions_jTable.getModel();
        int rows = model.getRowCount();
        for (int i = 0; i < rows; i++) {
            model.removeRow(0);
        }
        for (Auction auction : auctions) {
            model.addRow(new Object[]{auction.getId(), auction.getItem(), auction.getStatus(), auction.getOpenTime(), auction.getCloseTime(), auction.getItem().getSeller().getDisplayName(), auction.getIncrement()});
        }
    }
